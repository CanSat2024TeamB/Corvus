class Coordinates(Vector3d):
    def __init__(self, x: float, y: float, z: float):
        super(x, y, z)
    
    def __init__(self):
        super()

    def longitude(self) -> float:
        return self.x()
    
    def latitude(self) -> float:
        return self.y()
    
    def set_longitude(self, x: float) -> None:
        self.set_x(x)
        return
    
    def set_latitude(self, y: float) -> None:
        self.set_y(y)
        return